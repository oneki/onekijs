import { CollectionItemAdapter, QuerySearcher } from 'onekijs';
import { SelectItem } from 'onekijs-ui';

export interface User {
  id: number,
  firstname: string,
  lastname: string,
  address: {
    street: string,
    postalCode: number,
    city: string,
  }
  phones: string[],
}

export const userAdapter: CollectionItemAdapter<User, SelectItem<User>> = (data?: User) => {
  return {
    id: data ? String(data.id) : undefined,
    text: data ? `${data.firstname} ${data.lastname}` : '',
    data,
    meta: {},
  };
};

export const userSearcher: QuerySearcher<User> = (item, search) => {
  if (search) {
    return `${item.firstname} ${item.lastname}`.toUpperCase().includes(String(search).toUpperCase());
  }
  return true;
};

